export default function MonthConversion(month: number) {
  switch (month) {
    case 0:
      return "jan";
    case 1:
      return "fev";
    case 2:
      return "mar";
    case 3:
      return "abri";
    case 4:
      return "mai";
    case 5:
      return "jun";
    case 6:
      return "jul";
    case 7:
      return "ago";
    case 8:
      return "set";
    case 9:
      return "out";
    case 10:
      return "nov";
    case 11:
      return "dez";
  }
}
